const THINKING_ICONS = [BrainIcon, CpuIcon, SparklesIcon, AtomIcon, LightbulbIcon, ZapIcon];

export function ThinkingIndicator() {
  // Pick ONE random word + icon per mount — stable for the entire thinking session
  const [word] = useState(() => THINKING_WORDS[Math.floor(Math.random() * THINKING_WORDS.length)]);
  const [Icon] = useState(() => THINKING_ICONS[Math.floor(Math.random() * THINKING_ICONS.length)]);

  return (
    <div className="flex items-center gap-2 py-2">
      <Icon />
      <span className="text-sm animate-shimmer">{word}...</span>
    </div>
  );
}
